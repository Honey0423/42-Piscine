#include <unistd.h>

void	_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int	i;
	int	j;

	i = 0;
	while (i < 99)
	{
		j = i + 1;
		while (j < 100)
		{
			_putchar(i / 10 + '0');
			_putchar(i % 10 + '0');
			write(1, " ", 1);
			_putchar(j / 10 + '0');
			_putchar(j % 10 + '0');
			if (i != 98)
			{
				write(1, ", ", 2);
			}
			j++;
		}
		i++;
	}
}
