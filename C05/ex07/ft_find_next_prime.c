int	ft_find_next_prime(int nb)
{
	int	i;
	int	j;
	int	count;

	i = nb;
	while (1)
	{
		j = 1;
		count = 0;
		while (j <= i)
		{
			if (i % j == 0)
				count++;
			j++;
		}
		if (count == 2)
			return (i);
		i++;
	}
}
